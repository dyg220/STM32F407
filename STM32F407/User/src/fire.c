#include "fire.h"

/*********************************************************
*函 数 名：Fire_Init
*函数功能：火焰传感器初始化
*参    数：None
*返 回 值：None
*备    注：Fire---PA4
**********************************************************/
void Fire_Init(void)
{
	//时钟使能
	RCC->AHB1ENR |= (1<<0);
	//配置通用输入模式
	GPIOA->MODER &= ~(3<<8);
	//配置浮空输入
	GPIOA->PUPDR &= ~(3<<8);
	
}

